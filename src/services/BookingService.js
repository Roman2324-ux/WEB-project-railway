const STORAGE_KEY = "railway_bookings";

// ─── Helpers ──────────────────────────────────────────────────────────────────

function loadAll() {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

function saveAll(bookings) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(bookings));
}

